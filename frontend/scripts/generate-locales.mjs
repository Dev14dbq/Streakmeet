/** Deep merge override into base */
function merge(base, override) {
  const out = structuredClone(base)
  for (const [k, v] of Object.entries(override)) {
    if (v && typeof v === 'object' && !Array.isArray(v) && typeof out[k] === 'object') {
      out[k] = merge(out[k], v)
    } else {
      out[k] = v
    }
  }
  return out
}

